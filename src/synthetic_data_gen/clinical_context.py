import random
from utilities import choice, synonym

def generate_clinical_context(stage, diagnosis=None):
    # Предыдущее лечение
    if stage == "I":
        previous_treatment_dist = {
            "не получал": 0.80,
            "после операции": 0.20
        }
    elif stage in ["II", "III"]:
        previous_treatment_dist = {
            "не получал": 0.55,
            "после операции": 0.15,
            "после химиотерапии": 0.15,
            "после химиолучевой терапии": 0.15
        }
    else:
        previous_treatment_dist = {
            "не получал": 0.60,
            "после химиотерапии": 0.15,
            "прогрессирование после таргетной терапии": 0.10,
            "прогрессирование после иммунотерапии": 0.10,
            "после химиолучевой терапии": 0.05
        }

    previous_treatment = choice(previous_treatment_dist)

    # Линия терапии.
    # Нужна, чтобы отличать первую линию от второй/последующих линий.
    if previous_treatment == "не получал":
        treatment_line = "first_line"
    elif previous_treatment in ["после операции", "после химиолучевой терапии"]:
        treatment_line = choice({
            "adjuvant_or_consolidation": 0.50,
            "first_line": 0.30,
            "second_line": 0.20
        })
    else:
        treatment_line = choice({
            "second_line": 0.70,
            "third_or_later_line": 0.30
        })

    # Ответ на предыдущее лечение.
    # Нужен для сценариев наблюдения, продолжения терапии или смены линии.
    if previous_treatment == "не получал":
        treatment_response = "not_applicable"
    else:
        treatment_response = choice({
            "partial_response": 0.25,
            "stable_disease": 0.30,
            "progression": 0.35,
            "intolerable_toxicity": 0.10
        })

    # Характер прогрессирования.
    # Особенно важно при таргетной терапии: локальная/олигопрогрессия может вести к локальному лечению + продолжению TKI.
    if treatment_response == "progression" or "прогрессирование" in previous_treatment:
        progression_pattern = choice({
            "systemic": 0.55,
            "oligoprogression": 0.30,
            "CNS_only": 0.15
        })
    else:
        progression_pattern = "not_applicable"

    # Цель текущего обращения.
    # Нужна для генерации правильного типа рекомендации.
    clinical_goal = choice({
        "initial_treatment_selection": 0.45,
        "treatment_after_progression": 0.25,
        "adjuvant_or_consolidation_decision": 0.15,
        "symptom_control": 0.10,
        "follow_up": 0.05
    })

    return {
        "previous_treatment": previous_treatment,
        "treatment_line": treatment_line,
        "treatment_response": treatment_response,
        "progression_pattern": progression_pattern,
        "clinical_goal": clinical_goal
    }
    
