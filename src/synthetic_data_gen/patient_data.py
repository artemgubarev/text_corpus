def generate_patient():
    # пол
    sex_dist = {"male": 0.6, "female": 0.4}
    
    # курение
    smoking_dist = {
        "never": 0.15,
        "former smoker": 0.35,
        "current smoker": 0.30,
        "20 pack-years": 0.10,
        "40 pack-years": 0.07,
        "60 pack-years": 0.03
    }
    
    # состояние пациента
    ecog_dist = {
        0: 0.20, # полностью активен
        1: 0.45, # есть симптомы, но активен
        2: 0.25, # ограничен, но ходит
        3: 0.08, # ограничен, в основном лежит
        4: 0.02  # полностью лежачий 
    }
    
    # Сопутствующие заболевания.
    comorbidities_dist = {
        "none": 0.30,                    # нет значимых сопутствующих заболеваний
        "COPD": 0.25,                    # ХОБЛ; важно для оценки дыхательной функции и риска операции/лучевой терапии
        "hypertension": 0.20,            # гипертония; влияет на общий кардиориск
        "diabetes": 0.10,                # диабет; влияет на риск осложнений и переносимость лечения
        "coronary_artery_disease": 0.10, # ИБС; важно для оценки кардиориска перед операцией/химиотерапией
        "chronic_kidney_disease": 0.05   # ХБП; важно для выбора/ограничения цисплатина и дозировок
    }
    
    # Функция почек.
    renal_function_dist = {
        "normal": 0.75,              # функция почек сохранена
        "mild_impairment": 0.15,     # лёгкое снижение; часто лечение возможно с осторожностью
        "moderate_impairment": 0.08, # умеренное снижение; цисплатин может быть нежелателен
        "severe_impairment": 0.02    # тяжёлое снижение; высокие ограничения для химиотерапии
    }
    
    # Функция печени.
    liver_function_dist = {
        "normal": 0.85,              # функция печени сохранена
        "mild_impairment": 0.10,     # лёгкое нарушение
        "moderate_impairment": 0.04, # умеренное нарушение; нужна осторожность с терапией
        "severe_impairment": 0.01    # тяжёлое нарушение; системная терапия может быть ограничена   
    }

    # Кардиологический статус.
    cardiac_status_dist = {
        "stable": 0.85,              # нет значимых активных кардиологических проблем
        "controlled_disease": 0.10,  # есть заболевание, но оно контролируется
        "unstable": 0.05             # нестабильное состояние; операция/агрессивная терапия рискованны
    }

    # Выраженность боли.
    pain_dist = {
        "none": 0.35,     # боли нет
        "mild": 0.30,     # лёгкая боль
        "moderate": 0.25, # умеренная боль; требуется обезболивание
        "severe": 0.10    # выраженная боль; нужна активная симптоматическая/паллиативная помощь
    }
    
    # Симптомы
    symptoms_dist = {
        "кашель": 0.6,        # частый симптом рака легкого
        "одышка": 0.5,        # важна для оценки дыхательной недостаточности, выпота, обструкции
        "потеря веса": 0.4,   # отражает системное влияние опухоли и нутритивный статус
        "слабость": 0.5,      # влияет на оценку общего состояния пациента
        "боль в груди": 0.3   # может быть связана с плеврой, грудной стенкой или распространением опухоли
    }
    
    # Противопоказания к отдельным видам терапии.
    drug_contraindications_dist = {
        "none": 0.85,          # явных лекарственных противопоказаний нет
        "platinum": 0.10,      # ограничение для платиносодержащей химиотерапии
        "immunotherapy": 0.05  # ограничение для иммунотерапии
    }
    
    return {
        "age": random.randint(35, 85),  # возраст; влияет на переносимость лечения и хирургические риски
        "sex": choice(sex_dist),        # пол пациента
        "smoking_history": choice(smoking_dist),  # анамнез курения; фактор риска и клинический контекст
        "ECOG": choice(ecog_dist),      # функциональный статус; один из главных факторов выбора интенсивности лечения
        "comorbidity": choice(comorbidities_dist),  # сопутствующие заболевания; влияют на риски операции/ХТ/ЛТ
        "renal_function": choice(renal_function_dist),  # функция почек; критична для цисплатина и дозировок
        "liver_function": choice(liver_function_dist),  # функция печени; важна для метаболизма препаратов и токсичности
        "cardiac_status": choice(cardiac_status_dist),  # кардиологический статус; влияет на операбельность и переносимость терапии
        "pain_severity": choice(pain_dist),  # выраженность боли; нужна для симптоматической/паллиативной терапии
        "symptoms": [
            symptom for symptom, prob in symptoms_dist.items()
            if random.random() < prob
        ],  # список симптомов пациента
        "hemoptysis": choice({True: 0.15, False: 0.85}),  # кровохарканье; важно для оценки риска кровотечения
        "pleural_effusion": choice({True: 0.20, False: 0.80}),  # плевральный выпот; влияет на одышку и паллиативную тактику
        "weight_loss": choice({True: 0.4, False: 0.6}),  # потеря веса; отражает нутритивный статус и прогностический риск
        "BMI": round(random.uniform(18, 32), 1),  # индекс массы тела; нужен для оценки нутритивного статуса
        "oxygen_support": choice({True: 0.15, False: 0.85}),  # потребность в кислороде; признак дыхательной недостаточности
        "pregnancy": False,  # беременность; обычно False, но для женщин <50 можно генерировать отдельно
        "drug_contraindications": choice(drug_contraindications_dist),  # ограничения к терапии
        "hemoglobin": random.randint(90, 150),  # Hb; анемия влияет на переносимость ХТ и общее состояние
        "creatinine": random.randint(60, 140),  # креатинин; нужен для оценки функции почек и возможности цисплатина
        "AST": random.randint(10, 80),  # печеночный фермент; важен для оценки функции печени
        "ALT": random.randint(10, 80)   # печеночный фермент; важен для оценки функции печени
    }

