    metastases_text = (
        "Отдаленные метастазы не указаны."
        if not diagnosis["metastases"]
        else f"Метастатическое поражение: {', '.join(diagnosis['metastases'])}."
    )

    tumor_location_text = {
        "central": [
            "Опухоль центральной локализации.",
            "Локализация опухоли: центральная.",
            "Опухолевый процесс расположен центрально."
        ],
        "peripheral": [
            "Опухоль периферической локализации.",
            "Локализация опухоли: периферическая.",
            "Опухолевый процесс расположен периферически."
        ],
        "apical": [
            "Опухоль верхушки легкого.",
            "Локализация опухоли: верхушечная.",
            "Опухолевый процесс расположен в области верхушки легкого."
        ]
    }

    resectability_text = {
        "resectable": [
            "Опухоль расценивается как резектабельная.",
            "Процесс потенциально резектабелен.",
            "С онкологической точки зрения возможно рассмотрение радикального хирургического лечения."
        ],
        "borderline_resectable": [
            "Резектабельность процесса пограничная.",
            "Вопрос резектабельности требует дополнительной оценки.",
            "Операбельность опухоли требует обсуждения на мультидисциплинарном консилиуме."
        ],
        "unresectable": [
            "Процесс расценивается как нерезектабельный.",
            "Радикальное хирургическое лечение опухоли ограничено.",
            "Опухолевый процесс не рассматривается как резектабельный."
        ]
    }

    metastatic_burden_text = {
        "oligometastatic": [
            "Метастатическое поражение ограниченное.",
            "Олигометастатический характер заболевания.",
            "Имеется ограниченное число метастатических очагов."
        ],
        "multiple": [
            "Имеется множественное метастатическое поражение.",
            "Метастатический процесс распространенный.",
            "Отмечается множественный характер отдаленного поражения."
        ],
        "not_applicable": [
            "Оценка метастатической нагрузки неприменима.",
            "Метастатическая нагрузка не оценивается при отсутствии M1.",
            "Данных за отдаленное метастатическое поражение нет."
        ]
    }

    blocks = [
        f"Тип опухоли: {diagnosis['type']}.",
        f"Гистологический вариант: {diagnosis['histology']}.",
        f"Стадия заболевания: {diagnosis['stage']}.",
        f"TNM: {diagnosis['TNM']}.",
        f"Размер опухоли: {diagnosis['tumor_size_cm']} см.",
        synonym(tumor_location_text[diagnosis["tumor_location"]]),
        synonym(resectability_text[diagnosis["resectability"]]),
        metastases_text,
        synonym(metastatic_burden_text[diagnosis["metastatic_burden"]]),
        f"EGFR: {diagnosis['EGFR']}.",
        f"ALK: {diagnosis['ALK']}.",
        f"ROS1: {diagnosis['ROS1']}.",
        f"BRAF: {diagnosis['BRAF']}.",
        f"PD-L1: {diagnosis['PD-L1']}."
    ]

    if shuffle_blocks:
        random.shuffle(blocks)

    return " ".join(blocks)