import random
from utilities import choice, synonym

def generate_diagnosis():
    # Тип рака.
    cancer_type = choice({
        "немелкоклеточный рак легкого": 0.85,  # наиболее частый вариант рака легкого
        "мелкоклеточный рак легкого": 0.15    # более агрессивный вариант, обычно лечится по отдельным схемам
    })

    # Гистологический вариант.
    # Нужен для выбора тактики лечения: операция/ХТ/иммунотерапия/таргетная терапия.
    if cancer_type == "немелкоклеточный рак легкого":
        histology = choice({
            "аденокарцинома": 0.50,        # часто требует молекулярного тестирования EGFR/ALK/ROS1/BRAF
            "плоскоклеточный рак": 0.35,   # важен для выбора химиотерапии и оценки противопоказаний к некоторым препаратам
            "крупноклеточный рак": 0.05,   # более редкий вариант НМРЛ
            "НМРЛ неуточненный": 0.10      # используется, если подтип не определен
        })
    else:
        histology = choice({
            "мелкоклеточный рак": 0.95,                 # основной вариант МРЛ
            "комбинированный мелкоклеточный рак": 0.05  # редкий смешанный вариант
        })

    # Стадия заболевания.
    # Один из главных факторов выбора тактики лечения.
    stage = choice({
        "I": 0.15,    # ранняя стадия; чаще рассматривается хирургическое лечение
        "II": 0.15,   # локализованный процесс; операция +/- адъювантная терапия
        "III": 0.25,  # местнораспространенный процесс; часто комбинированное лечение
        "IV": 0.45    # метастатический процесс; системная/паллиативная терапия
    })

    # TNM.
    # Нужно для более точного описания стадии.
    tnm_by_stage = {
        "I": [
            "T1N0M0",   # небольшая опухоль без поражения лимфоузлов и метастазов
            "T2aN0M0"   # опухоль большего размера без лимфоузлов и метастазов
        ],
        "II": [
            "T2bN0M0",  # опухоль без лимфоузлов, но более выраженная T-категория
            "T1N1M0",   # опухоль с поражением регионарных лимфоузлов N1
            "T2N1M0"    # опухоль T2 с поражением N1
        ],
        "III": [
            "T2N2M0",   # поражение средостенных лимфоузлов
            "T3N2M0",   # местнораспространенная опухоль с N2
            "T4N1M0",   # местное распространение опухоли при N1
            "T4N2M0"    # местнораспространенный процесс с N2
        ],
        "IV": [
            "T1N2M1b",  # отдаленный единичный метастаз
            "T2bN2M1b", # опухоль T2b с N2 и единичным отдаленным метастазом
            "T3N3M1c",  # множественные отдаленные метастазы
            "T4N3M1c"   # распространенная опухоль с N3 и множественными метастазами
        ]
    }

    tnm = random.choice(tnm_by_stage[stage])

    # Метастазы.
    # Генерируются только при IV стадии.
    if stage == "IV":
        possible_metastases = [
            "головной мозг",             # влияет на необходимость МРТ/локального лечения/ЛТ
            "кости",                     # требует обезболивания и профилактики костных осложнений
            "печень",                    # может влиять на функцию печени и прогноз
            "плевра",                    # может сопровождаться плевральным выпотом/одышкой
            "надпочечники",              # частая зона метастазирования
            "контралатеральное легкое"   # соответствует распространенному процессу
        ]
        metastases = random.sample(possible_metastases, k=random.randint(1, 3))
    else:
        metastases = []  # при I–III стадии отдаленных метастазов быть не должно

    # Молекулярные маркеры.
    # Нужны для выбора таргетной терапии и иммунотерапии.
    if cancer_type == "немелкоклеточный рак легкого":
        EGFR = choice({
            "positive": 0.12,  # при наличии мутации возможна EGFR-таргетная терапия
            "negative": 0.88
        })

        ALK = "negative"
        ROS1 = "negative"
        BRAF = "negative"

        # Драйверные мутации делаем взаимоисключающими,
        # чтобы не получать клинически сомнительные комбинации.
        if EGFR == "negative":
            ALK = choice({
                "positive": 0.05,  # при наличии ALK-перестройки возможна ALK-таргетная терапия
                "negative": 0.95
            })

        if EGFR == "negative" and ALK == "negative":
            ROS1 = choice({
                "positive": 0.02,  # при наличии ROS1 возможна ROS1-таргетная терапия
                "negative": 0.98
            })

        if EGFR == "negative" and ALK == "negative" and ROS1 == "negative":
            BRAF = choice({
                "positive": 0.02,  # при BRAF V600E возможна комбинация BRAF/MEK ингибиторов
                "negative": 0.98
            })

        PD_L1 = choice({
            "unknown": 0.10,  # статус не определен
            "0%": 0.15,      # экспрессия отсутствует
            "1-49%": 0.45,   # низкая/промежуточная экспрессия
            ">=50%": 0.30    # высокая экспрессия; может влиять на выбор иммунотерапии
        })
    else:
        # Для МРЛ эти маркеры в данной синтетической схеме не используются.
        EGFR = "not_applicable"
        ALK = "not_applicable"
        ROS1 = "not_applicable"
        BRAF = "not_applicable"
        PD_L1 = "not_applicable"
        
    # Локализация опухоли.
    tumor_location = choice({
        "central": 0.45,     # центральная опухоль; чаще кашель, кровохарканье, обструкция бронха
        "peripheral": 0.45,  # периферическая опухоль; может дольше протекать бессимптомно
        "apical": 0.10       # верхушечная опухоль; возможен синдром Панкоста
    })
    
    # Размер опухоли.
    tumor_size_cm = round(random.uniform(1.0, 8.0), 1)
    
    # Резектабельность / операбельность процесса.
    if stage in ["I", "II"]:
        resectability = choice({
            "resectable": 0.85,
            "borderline_resectable": 0.10,
            "unresectable": 0.05
        })
    elif stage == "III":
        resectability = choice({
            "resectable": 0.25,
            "borderline_resectable": 0.35,
            "unresectable": 0.40
        })
    else:
        resectability = "unresectable"

        # Количество/характер метастазов.
    if stage == "IV":
        metastatic_burden = choice({
            "oligometastatic": 0.25,  # ограниченное число метастазов
            "multiple": 0.75          # множественное метастатическое поражение
        })
    else:
        metastatic_burden = "not_applicable"

    return {
        "type": cancer_type,
        "histology": histology,
        "stage": stage,
        "TNM": tnm,
        "tumor_location": tumor_location,
        "tumor_size_cm": tumor_size_cm,
        "resectability": resectability,
        "metastases": metastases,
        "metastatic_burden": metastatic_burden,
        "EGFR": EGFR,
        "ALK": ALK,
        "ROS1": ROS1,
        "BRAF": BRAF,
        "PD-L1": PD_L1
    }
    
